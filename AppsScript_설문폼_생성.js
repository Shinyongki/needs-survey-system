function createAllForms() {
  Logger.log('=== 설문폼 생성 시작 ===');

  // 통합설문 2종
  createS01();
  createS02();

  // 현장설문 16종
  const jCodes = ['J01','J03','J04','J05','J06','J07','J08',
                  'J09','J10','J11','J12','J13','J14','J15','J16','J17'];
  jCodes.forEach(function(code) {
    createJForm(J_SPECS[code]);
    Utilities.sleep(1000); // API 제한 방지
  });

  Logger.log('\n=== 생성 완료 ===');
  Logger.log('총 ' + LOG.length + '개 폼 생성\n');
  LOG.forEach(function(msg) { Logger.log(msg); });
}

/**
 * 단일 폼만 재생성 (수정 필요 시)
 * 예: recreateForm('J01')
 */
function recreateForm(code) {
  if (code === 'S01') { createS01(); return; }
  if (code === 'S02') { createS02(); return; }
  if (J_SPECS[code])  { createJForm(J_SPECS[code]); return; }
  Logger.log('알 수 없는 코드: ' + code);
}
