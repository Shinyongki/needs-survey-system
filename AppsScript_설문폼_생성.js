const SCALE5 = ['① 매우 그렇다', '② 그렇다', '③ 보통', '④ 그렇지 않다', '⑤ 매우 그렇지 않다'];

function createJForm(spec) {
  const formTitle = spec.code + '_' + spec.title + '_' + spec.target + '_2026';
  const form = FormApp.create(formTitle);

  setupForm(form,
    '[' + spec.code + '] ' + spec.title + ' (' + spec.target + ')',
    '경상남도사회서비스원 노인맞춤돌봄서비스 광역지원기관\n' +
    '응답 대상: ' + spec.target + ' | ' +
    (spec.code === 'J04' || spec.code === 'J06' ? '총 14문항' : '총 13문항') + '\n' +
    '※ 이 설문은 익명으로 처리됩니다. 해당 사업 참여 직후 응답해 주세요.'
  );

  // 도입부 (Q01~Q03)
  addJIntro(form);

  // 공통 만족도 (Q04~Q08)
  addJCommon(form, spec.title);

  // 특화 문항 (Q09~)
  addSection(form, '특화 문항', '이번 ' + spec.title + '의 세부 내용을 평가해 주세요.');

  spec.specialized.forEach(function(q, idx) {
    const qNum = 9 + idx;
    if (q.type === 'scale') {
      addScale(form, qNum, q.text, SCALE5);
    } else if (q.type === 'mc') {
      addMC(form, qNum, q.text, q.choices,
        q.isNeeds ? '※ 다음 회차 프로그램 기획에 반영됩니다.' : null);
    } else if (q.type === 'para') {
      addPara(form, qNum, q.text);
    }
  });

  logForm(spec.code, spec.title + ' (' + spec.target + ')', form.getPublishedUrl());
}

